def won(n):
    return f"{n:,}"


def split12(total):
    if total <= 0:
        return [0] * 12
    base = total // 12
    arr = [base] * 12
    arr[-1] += total - base * 12
    return arr


def render(filename, lines):
    path = os.path.join(OUT, filename)
    c = canvas.Canvas(path, pagesize=A4)
    c.setFont(FONT, FS)
    y = TOP
    for ln in lines:
        if y < 42:
            c.showPage(); c.setFont(FONT, FS); y = TOP
        c.drawString(MX, y, ln)
        y -= LH
    c.showPage(); c.save()
    return path


