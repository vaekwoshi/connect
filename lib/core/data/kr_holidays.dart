/// 한국 공휴일 — 달력 색칠용(휴대폰 기본 달력처럼 일요일·공휴일은 빨강).
/// 양력 고정 공휴일 + 연도별 음력/대체공휴일(2025·2026)을 담는다.
class KrHolidays {
  /// 매년 동일한 양력 공휴일 (MM-DD).
  static const Set<String> _fixed = {
    '01-01', // 신정
    '03-01', // 삼일절
    '05-05', // 어린이날
    '06-06', // 현충일
    '08-15', // 광복절
    '10-03', // 개천절
    '10-09', // 한글날
    '12-25', // 성탄절
  };

  /// 음력 기반·대체공휴일 등 연도별 공휴일 (YYYY-MM-DD).
  static const Set<String> _variable = {
    // ── 2025 ──
    '2025-01-28', '2025-01-29', '2025-01-30', // 설날 연휴
    '2025-03-03', // 삼일절 대체
    '2025-05-06', // 어린이날·부처님오신날 대체
    '2025-10-05', '2025-10-06', '2025-10-07', '2025-10-08', // 추석 연휴+대체
    // ── 2026 ──
    '2026-02-16', '2026-02-17', '2026-02-18', // 설날 연휴
    '2026-03-02', // 삼일절(일) 대체
    '2026-05-24', '2026-05-25', // 부처님오신날 + 대체
    '2026-08-17', // 광복절(토) 대체
    '2026-09-24', '2026-09-25', '2026-09-26', // 추석 연휴
    '2026-10-05', // 개천절(토) 대체
  };

  static String _mmdd(DateTime d) =>
      '${d.month.toString().padLeft(2, '0')}-${d.day.toString().padLeft(2, '0')}';
  static String _ymd(DateTime d) =>
      '${d.year}-${d.month.toString().padLeft(2, '0')}-${d.day.toString().padLeft(2, '0')}';

  /// 해당 날짜가 공휴일이면 true.
  static bool isHoliday(DateTime d) =>
      _fixed.contains(_mmdd(d)) || _variable.contains(_ymd(d));
}
