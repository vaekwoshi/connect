class _SmeYouthEligibilityBanner extends StatefulWidget {
  const _SmeYouthEligibilityBanner();
  @override
  State<_SmeYouthEligibilityBanner> createState() =>
      _SmeYouthEligibilityBannerState();
}

class _SmeYouthEligibilityBannerState
    extends State<_SmeYouthEligibilityBanner> {
  late Future<Map<String, dynamic>?> _future;

  @override
  void initState() {
    super.initState();
    _future = dbService.getProfile();
  }

  @override
  Widget build(BuildContext context) {
    final accent = AppTheme.accentColor(context);
    final sub = AppTheme.inkSecondary(context);
    final line = AppTheme.line(context);
    return FutureBuilder<Map<String, dynamic>?>(
      future: _future,
      builder: (context, snap) {
        if (snap.connectionState == ConnectionState.waiting) {
          return const SizedBox.shrink();
        }
        final profile = snap.data;
        if (profile == null || (profile['age'] as int? ?? 0) == 0) {
          return _banner(context,
              icon: Icons.person_outline,
              text: '프로필을 먼저 입력해주세요',
              color: sub,
              borderColor: line);
        }
        final isSme = (profile['is_sme_employee'] as int? ?? 0) == 1;
        if (!isSme) {
          return _banner(context,
              icon: Icons.info_outline,
              text: '중소기업 재직자만 신청 가능',
              color: sub,
              borderColor: line);
        }
        final age = profile['age'] as int? ?? 0;
        final militaryMonths = profile['military_months'] as int? ?? 0;
        final smeStartYear = profile['sme_start_year'] as int? ?? 0;
        final isYouth = EmployeeTaxCalculator.isYouthSmeEligible(
            age: age, militaryMonths: militaryMonths);
        if (!isYouth) {
          return _banner(context,
              icon: Icons.info_outline,
              text: '만 34세 초과 — 청년 트랙 해당 없음',
              color: sub,
              borderColor: line);
        }
        final yearsWorked =
            smeStartYear > 0 ? DateTime.now().year - smeStartYear : -1;
        if (yearsWorked >= 5) {
          return _banner(context,
              icon: Icons.info_outline,
              text: '감면 기간(5년) 초과',
              color: sub,
              borderColor: line);
        }
        final remaining = yearsWorked >= 0 ? 5 - yearsWorked : 5;
        return _banner(context,
            icon: Icons.check_circle_outline,
            text: '감면 대상 · 90% 감면 · 잔여 ${remaining}년',
            color: accent,
            borderColor: accent);
      },
    );
  }

  Widget _banner(BuildContext context,
      {required IconData icon,
      required String text,
      required Color color,
      required Color borderColor}) {
    return Container(
      width: double.infinity,
      padding: const EdgeInsets.symmetric(horizontal: 12, vertical: 10),
      decoration: BoxDecoration(
          border: Border.all(color: borderColor),
          borderRadius: BorderRadius.circular(4)),
      child: Row(children: [
        Icon(icon, size: 15, color: color),
        const SizedBox(width: 8),
        Expanded(
            child: Text(text,
                style:
                    AppTheme.sans(13, color, weight: FontWeight.w500))),
      ]),
    );
  }
